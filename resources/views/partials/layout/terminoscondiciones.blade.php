<p><strong>Dato de registro</strong></p>
<p>
    Queremos informarle que siempre que utilice nuestro Servicio, en caso de error en la aplicación, recopilamos datos e
    información (a través de productos de terceros) en su teléfono llamado Datos de Registro. Estos pueden incluir
    información como la dirección del Protocolo de Internet ("IP") de su dispositivo, el nombre del dispositivo, la
    versión del sistema operativo, la configuración de la aplicación cuando utiliza nuestro Servicio, la fecha y hora de
    uso del Servicio y otras estadísticas.
</p>
<p><strong>Cookies</strong></p>
<p>
    Las cookies son archivos con una pequeña cantidad de datos que se utilizan comúnmente como identificadores únicos
    anónimos. Estos se envían a su navegador desde los sitios web que visita y se almacenan en la memoria interna de su
    dispositivo.
</p>
<p>
    Este servicio no utiliza estas "cookies" explícitamente. Sin embargo, la aplicación puede usar códigos de terceros y
    bibliotecas que usan "cookies" para recopilar información y mejorar sus servicios. Tiene la opción de aceptar o
    rechazar estas cookies y saber cuándo se envía una cookie a su dispositivo. Si decide rechazar nuestras cookies, es
    posible que no pueda utilizar algunas partes de este Servicio.
</p>
